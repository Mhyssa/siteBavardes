              s'auto-dissout après 5 ans d'existence en raison de la lourdeur et de l'ampleur des tâches administratives, 
              de la difficulté à transmettre les responsabilités associatives et politiques et de trop nombreuses sollicitations 
              auxquelles il est difficile de répondre de manière qualitative. <br><br>
              Le collectif a décidé de ne pas poursuivre son développement en reconduisant du salariat pour répondre aux sollicitations ou de ralentir ses activités. <br>
              À la place, il va se dissoudre et former deux groupes de travail: l'un pour assurer la suite du projet à Amiens 
              et l'autre pour écrire un manifeste/livre sur l'histoire du collectif.  <br><br>
